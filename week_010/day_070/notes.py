# Bash commands

# ls: List directory contents.
# cd: Change the current directory.
# pwd: Print the current working directory.
# mkdir: Create a new directory.
# rm: Remove files or directories.
# cp: Copy files or directories.
# mv: Move or rename files or directories.
# touch: Create an empty file or update file timestamps.
# cat: Display the contents of a file or concatenate files.
# grep: Search for patterns in files.
# echo: Display a line of text.
# chmod: Change file permissions.
# chown: Change file owner and group.
# sudo: Execute a command with superuser privileges.
# man: Display the manual page for a command.
# apt-get/yum: Package management commands for installing, updating, and removing software packages
# ps: Display information about processes.
# kill: Terminate processes.
# df: Display disk space usage.
# du: Display directory space usage.

# Github

# git init: Initialize a new Git repository in the current directory.
# git clone: Clone a repository into a new directory.
# git add: Add file contents to the index for staging.
# git commit: Record changes to the repository.
# git status: Show the status of files in the repository.
# git diff: Show changes between commits, commit and working tree, etc.
# git log: Show commit logs.
# git pull: Fetch from and integrate with another repository or a local branch.
# git push: Update remote refs along with associated objects.
# git branch: List, create, or delete branches.
# git checkout: Switch branches or restore working tree files.
# git merge: Join two or more development histories together.
# git remote: Manage set of tracked repositories.
# git stash: Stash the changes in a dirty working directory away.
# git tag: Create, list, delete, or verify a tag object signed with GPG.
# git fetch: Download objects and refs from another repository.
# git rebase: Reapply commits on top of another base tip.
# git reset: Reset current HEAD to the specified state.
# git revert: Revert some existing commits.
# git config: Get and set repository or global options.
